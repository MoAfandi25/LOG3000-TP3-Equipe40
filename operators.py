def add(a, b):
    """
    Additionne deux nombres.
    
    Args:
        a (float): Le premier nombre à additionner.
        b (float): Le deuxième nombre à additionner.
    
    Returns:
        float: La somme de a et b.
    """
    return a + b

def subtract(a, b):
    """
    Soustrait le deuxième nombre du premier nombre.
    
    Args:
        a (float): Le nombre duquel soustraire.
        b (float): Le nombre soustrait
    
    Returns:
        float: Le résultat de a - b.
    """
    return a - b

def multiply(a, b):
    """
    Mutliplie a par b
    
    Args:
        a (float): Premier facteur
        b (float): Second facteur
    
    Returns:
        float: Le résultat de la multiplication de a par b
    """
    return a * b

def divide(a, b):
    """
    Effectue la division décimale du premier nombre (a) par le deuxième nombre (b).
    
    Args:
        a (float): Le dividende (nombre à diviser).
        b (float): Le diviseur.
    
    Returns:
        float: Le résultat de la division de a par b (a / b).
    """
    return a / b
